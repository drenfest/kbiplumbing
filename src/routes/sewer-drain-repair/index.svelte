<h3>Backwater Valves & Backflow Prevention</h3>
<p>You rely on fresh water coming out of the faucets in your house and other distribution lines (to your own laundry machines, dishwashers, etc.) for many daily tasks. The probability of losing fresh water provides an unpleasant thought. Did you know that your water can be cross contaminated? Backflow valves provide that from happening. Although it is an inexpensive fix you definetly want to have a professional plumber checking your backflow system.</p>
<p>Most houses are equipped with a system that prevents wastewater in the sewage system backing up into the new water source. Backflow prevention devices must get routine testing from accredited plumbers to be certain that they’re working properly and not at risk of failing. You’ll want the aid of a certified professional plumber to fix or install a backflow preventer if necessary.</p>
<h3>Battery Backup Sump Pump Protection</h3>
<p>Ensure your house is guarded with a trusted battery backup machine from the professionals at KBI Plumbing we provide two kinds of battery backup systems for our clients. The first is a battery powered system to operate your main sump pump whenever you don’t require a pump. Our battery system may operate for up to two weeks depending on how far it runs, the dimensions of the sump pump, and also the elevation of release. Countless basements have flooded because of a power outage.</p>
<p>Our second kind is a battery backup sump pump. It’s a battery operated backup that is set up next to your primary pump.</p>
